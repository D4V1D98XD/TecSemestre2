package Productos;

public class Productos {

	private String fecha;
	private int lote;
	private String pais;
	private String envasado;
	
	public Productos() {
		
	}

	public Productos(String fecha, int lote, String pais, String envasado) {
		this.fecha = fecha;
		this.lote = lote;
		this.pais = pais;
		this.envasado = envasado;
	}
	
	
}

